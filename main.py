def task3_20():
    # Імпортувати модуль math для використання константи e і функції sqrt
    import math

    # Ввести кількість членів суми
    k = int(input("Введіть кількість членів суми: "))

    # Ініціалізувати змінну для зберігання суми
    s = 0

    # Використати цикл for для обчислення суми
    for n in range(1, k + 1):
        # Обчислити n-й член суми
        t = math.e ** n / n ** math.sqrt(n / 2)
        # Додати член до суми
        s += t

    # Вивести результат
    print(f"\nСума {k} членів ряду = {s}")

task3_20()