def task1_if20():
    # Ввести координати точок A, B, C
    a = float(input("Введіть координату точки A: "))
    b = float(input("Введіть координату точки B: "))
    c = float(input("Введіть координату точки C: "))

    # Обчислити відстані від A до B і C
    d_ab = abs(a - b) # Модуль різниці координат
    d_ac = abs(a - c)

    # Порівняти відстані і вивести результат
    if d_ab < d_ac:
        print(f"\nТочка B ближче до A, відстань = {d_ab}\n")
    elif d_ab > d_ac:
        print(f"\nТочка C ближче до A, відстань = {d_ac}\n")
    else:
        print(f"\nТочки B і C рівновіддалені від A, відстань = {d_ab}\n")

task1_if20()


